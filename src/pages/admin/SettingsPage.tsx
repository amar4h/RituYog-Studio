export function SettingsPage() {
  const [activeTab, setActiveTab] = useState<SettingsTab>('whatsapp');
  const [error, setError] = useState('');
  const [success, setSuccess] = useState('');
  const [loading, setLoading] = useState<string | null>(null);

  const tabProps = { error, setError, success, setSuccess, loading, setLoading };

  return (
    <div className="space-y-6">
      {/* Header with Tab Navigation */}
      <div>
        <h1 className="text-2xl font-bold text-gray-900">Settings</h1>
        <p className="text-gray-600 mb-4">Manage your studio settings and preferences</p>

        {/* Tab Navigation */}
        <div className="border-b border-gray-200">
          <nav className="flex space-x-1 overflow-x-auto pb-px" aria-label="Settings tabs">
            {SETTINGS_TABS.map((tab) => (
              <button
                key={tab.id}
                onClick={() => setActiveTab(tab.id)}
                className={`flex items-center gap-2 px-4 py-3 text-sm font-medium border-b-2 whitespace-nowrap transition-colors ${
                  activeTab === tab.id
                    ? 'border-indigo-500 text-indigo-600'
                    : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
                }`}
              >
                {tab.icon}
                {tab.label}
              </button>
            ))}
          </nav>
        </div>
      </div>

      {error && (
        <Alert variant="error" dismissible onDismiss={() => setError('')}>
          {error}
        </Alert>
      )}

      {success && (
        <Alert variant="success" dismissible onDismiss={() => setSuccess('')}>
          {success}
        </Alert>
      )}

      {activeTab === 'studio' && <StudioTab {...tabProps} />}
      {activeTab === 'memberships' && <MembershipsTab {...tabProps} />}
      {activeTab === 'invoices' && <InvoicesTab {...tabProps} />}
      {activeTab === 'whatsapp' && <WhatsAppTab {...tabProps} />}
      {activeTab === 'holidays' && <HolidaysTab {...tabProps} />}
      {activeTab === 'legal' && <LegalTab {...tabProps} />}
      {activeTab === 'security' && <SecurityTab {...tabProps} />}
      {activeTab === 'data-tools' && <DataToolsTab {...tabProps} />}
    </div>
  );
}
